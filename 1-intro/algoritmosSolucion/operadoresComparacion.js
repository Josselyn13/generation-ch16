//Operadores de comparación

//Igualdad
console.log(5 == 5);
console.log(1 == 3);

console.log(5 == "cinco");
console.log(5 == "5"); //caso especial (aquí se usa el estrictamente igual)
console.log(0 === false);

console.log("hola" == "adios");
console.log("hola" == "hola");

//Desigualdad

console.log(5 !== "5");
console.log(5 != 1);
console.log("gato" != "gato");

//Mayor
console.log(10 > 5);
console.log(10 < 20);
console.log(5 > 5);

//mayor o igual
console.log(5 >= 5);
console.log(10 >= 3);
console.log(3 >= 7);

//Menor
console.log(10 < 20);
console.log(20 < 10);

//Menor o igual
console.log(20 <= 20);


