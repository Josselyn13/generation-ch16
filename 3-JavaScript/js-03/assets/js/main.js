/* Bucles */
/* Estructuras que nos permiten repetir nuestro código */

/* For */
/* Es un ciclo controlado */
/* Se le tiene que indicar:
        -la variable con la que se va a trabajar: i, j, k, l 
        -condición para que se ejecute
        -Cambio de nuestra variable de trabajo*/



for (let i = 0; i < 10; i++) {
    console.log("El valor de i es "+ i);
    
}

/* While */
/* Porque es un ciclo no controlado */
/* Se necesita:
        -Una condición => True o False */

let saludo;

while(saludo !== "Hola") {
    saludo = prompt("Saludame");
}


